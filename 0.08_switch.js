// switch is a function that changes a variable to something else if it meets certain criteria. break ends the function. 
let player = "Paul George"
switch	(player) {
	case 'Lebron James':
		console.log("Lebron is the second best ever");
		break;
	case 'Michael Jordan':
		console.log("the GOAT");
		break;
	case 'Paul George':
		console.log("PG-13 gave up on the pacers");
		break;
	default:
		console.log("I don't know who that is");
}

